
public class Loop {
  public static void startAttendence(int rollNo) {
    System.out.println("Roll No: " + rollNo + " is present.");
  }

  public static void main(String[] args) {
   int i = 1;
   for (System.out.println("Starting attendance..."); i <= 10; i++) {
      startAttendence(i);


    }
  }
}


 
   

   
 


